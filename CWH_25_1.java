public class CWH_25_1 {
    public static void main(String[] args) {
        int n=5;
        for(int i=n;i>0;i--)
        {
            for(int j=0;j<n;j++){
                System.out.print("* ");
            }
            System.out.println();
            n--;
        }
    }
}
